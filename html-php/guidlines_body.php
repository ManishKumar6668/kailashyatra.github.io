            <h3 class="h4 mb-20">Temperature & Weather</h3>
            <p class="mb-4">Weather conditions from May to September in Tibet, can be expected to be dry and sunny, with
                precipitants in July and August, day time temperatures can be as high as 24 degrees centigrade in Tibet.
                The average daily temperature will be 10 degrees centigrade, however, the intense sun at high altitude
                can make it feel even hotter. There will be incredible drop in temperature from early afternoon to
                evening time, temperature at night-time can drop to almost 3-4 degrees centigrade at many places and can
                go negative at places above 18000 feet.</p>

            <h3 class="h4 mb-20">Health Considerations</h3>
            <h3 class="h6 mb-20">Fitness : Planning & preparation</h3>
            <p class="mb-4">Please start to get in shape NOW!!! A good 30 minutes walk, jogging daily, stretching and
                regular exercise should put you in the right shape (Concentrate on your heart & legs Aerobics).You need
                to increase your walking habits, start doing some breathing exercises (Pranayama) with the consultation
                from your doctor. Reduce drinking alcohol & smoking habits. Consult your family doctor to assess the
                state of your health for functioning safely within your own limitations.</p>

