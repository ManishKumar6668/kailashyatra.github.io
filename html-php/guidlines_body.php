            <h3 class="h4 mb-20">Permit & Visa Procedures</h3>
            <h3 class="h6 mb-20">For Indian Nationals</h3>
            <p class="mb-4">Apply for Tibet permit (photocopy of your passport along with your occupation details are
                required)
                Procure the permit from Lhasa (usually takes 15-20 working days)
                Apply for Chinese Visa based on the permit at Chinese embassy in New Delhi (require original passport)
                Passport usually delivered at our office after getting Group Visa (any delay, can collect at Kathmandu,
                you can reach Kathmandu with a Voter ID; Passport not compulsory for Nepal)
                Don't require Nepali Visa.</p>

