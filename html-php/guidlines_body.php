            <h3 class="h4 mb-20">Travel Insurance</h3>
            <p class="mb-4">Kindly ensure that you are adequately insured for the full duration of the trip in respect
                of illness, injury, emergency coverage, death, loss of baggage and personal items and cancellation and
                curtailment. We can assist you in availing Travel Insurance.</p>
            <h3 class="h4 mb-20">Medical Fitness Certificate</h3>
            <p class="mb-4">You must be physically fit for Kailash Yatra. It is mandatory to provide a medical
                certificate. It is better for everyone to take an advice from your family doctor for traveling in high
                altitude. Medical Certificate Format can be downloaded from our Website. <a
                    href="images/MedicalCertificateFormat.pdf">Click Here to Download</a> </p>
            <h3 class="h4 mb-20">Altitude Sickness & Emergency</h3>
            <p class="mb-4">At high terrains, symptoms of altitude sickness (headache, loss of appetite, nausea,
                exhaustion, sleeplessness, breathlessness etc. – pilgrims usually acclimatize in a couple of days. You
                can avail the facilities of the Group's Doctor / Medical Officer. <br>
                We carry Oxygen Cylinders with Oxygen Pillows, wherein the Cylinders act as a reservoir for the pillows.
                You can also purchase Portable Oxygen Cylinders at Nyalam for 40 or 50 yuans <br>
                In case of non-acclimatization, the person will be transported to a lower altitude. The decision taken
                by the Chinese guide and Tour manager are binding on the yatris..
            </p>
